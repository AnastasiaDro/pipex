#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*p;

	if (n == 0)
		return ;
	i = 0;
	p = s;
	while (i < n)
	{
		*(p + i) = 0;
		i++;
	}
}
